function getCount(str) {
    var vowelsCount = 0;
    
    if(str === "a") {
        vowelsCount++
    } else if(str === "e") {
        vowelsCount++
    } else if(str === "i") {
        vowelsCount++
    } else if(str === "o") {
        vowelsCount++
    } else {
        vowelsCount++
    }
    
    return vowelsCount;
  }